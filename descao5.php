    <h1>Furfur</h1>
<h3>Origin:</h3>
<p>
Before his fall, Furfur, also known as Furtur, was an angelic protector of romantic relationships. However, his obsession with dark secrets and divine truths led him to challenge celestial authority. He sought to unveil to humans the mystery of duality between love and hate, believing that passion was the true power of the universe. This betrayal led to his eternal banishment from the divine realm.
</p>
<br>

<h3>Characteristics:</h3>
<p>
Furfur is a being of duality. He primarily manifests in the form of a pig with a fiery tail, symbolizing indulgence and destruction. However, within a protective triangle, he reveals his angelic form, a vision of purity and majesty. Although he rarely speaks the truth, his raspy voice resonates with power and authority.
</p>
<br>
<h3>Dominion:</h3>
<p>
The demonic count rules over the powers of love and passion, as well as the elements of weather. He can provoke stormy tempests and devastating lightning, and he has the ability to influence human emotions, especially in romantic relationships, making them intense and often destructive.
</p>
<br>
<h3>Legions:</h3>
<p>
Furfur governs over 26 legions of spirits, many of which are linked to elements and emotions. Each of these spirits seeks to extend its influence in the world, feeding on passion and chaos.
</p>
<br>
<h3>Prophecies:</h3>
<p>
Legend has it that during the last great storm to ravage the earth, Furfur will emerge to reveal a divine secret. This knowledge could either save or condemn humanity, depending on how it is used.
</p>
<br>
<h3>Invocation:</h3>
<p>
Summoning Furfur promises dark knowledge and passionate powers. To call upon him, one must draw his symbol in the center of a triangle and recite:

''Furfur, guardian of passions, reveal the hidden truth and ignite the flame of my desire''.

In return, the summoner must offer a secret never before shared or sacrifice a personal passion.
</p>
<br>
<h3>Protection:</h3>
<p>     
Playing with passions can be dangerous. Those who deal with Furfur may find themselves consumed by obsessions and unrestrained desires. The best protection against his influences is to carry a blue agate stone and recite purification mantras at dawn and dusk. In extreme situations, the intervention of an expert in exorcism may be necessary.
</p>
