    <h2>The Sucidal Hotel</h2>
    <p>The Cecil was built in 1924 by three hoteliers—William Banks Hanner, Charles L. Dix and Robert H. Schops—as a destination for business travelers and tourists. Designed by Loy Lester Smith in the Beaux Arts (or Art déco) style and constructed by W. W. Paden; the hotel cost $1.5 million to complete and boasted an opulent marble lobby with stained-glass windows, potted palms, and alabaster statuary. The three hoteliers invested about $2.5 million in the enterprise, with the knowledge that several similar hotels had been established elsewhere downtown, but within five years of its opening, the United States sank into the Great Depression. Although the hotel flourished as a fashionable destination throughout the 1940s,[citation needed] the decades beyond saw the hotel decline, as the nearby area known as Skid Row became increasingly populated with transients. As many as 10,000 homeless people lived within a four-mile (6 km) radius.

        <br><br>In 2008, a portion of the hotel was refurbished after new owners took over from there.[17]
        
        <br><br>In 2011, part of the Cecil Hotel was rebranded as "Stay on Main",[18] with separate reception areas during the day, but with shared facilities[19] and its official website remained thececilhotel.com.
        
        <br><br>In 2014, the hotel was sold to New York City hotelier Richard Born for $30 million,[20] after which another New York-based firm, Simon Baron Development, acquired a 99-year ground lease on the property.[5] In 2016, Matt Baron, president of Simon Baron, said he was committed to the preservation of architecturally or historically significant components of the building, such as the hotel's grand lobby, but his company planned to completely redevelop the interior and fix the "hodgepodge" work that had been done in more recent years.[21] The hotel closed in 2017 for the renovation, but the work was suspended indefinitely when the COVID-19 pandemic hit.
        
        <br><br>In February 2017, the Los Angeles City Council voted to deem the Cecil a Historic-Cultural Monument, because it is representative of an early 20th-century American hotel and because of the historic significance of its architect's body of work.
        
        <br><br>On December 13, 2021, the Cecil Hotel reopened as an affordable housing complex operated by the Skid Row Housing Trust. The facility will provide affordable living accommodations for 600 low-income residents.
        
        <br><br>In April 2022, the historic Cecil Hotel room rate advertisement painted on its southern façade was illegally white-washed. Although it is unclear whether the owners, the lessees, or the sub-lessees were responsible for the removal of the protected landmark's signage, it was likely removed to make way for a new mural or billboard. It was reported that artist Matthew Garcia was commissioned to paint a mural on the building. Additionally, an artist's rendering of billboards designed by Found Design for Simon Baron Development and organized by media developer Kevani, in which the project is entitled "The Frames," has been in circulation. Kevani's website indicates that the billboards are "coming soon." The original advertisement had been on the building since at least 1927. It originally read "HOTEL CECIL LOW MONTHLY WEEKLY RATES 700 ROOMS" with monthly later changed to daily. The remains of the first letters can still be seen and this is why the current word "daily" is aligned on the right side unlike the other rows.
  
        <br><br>In 1931 a guest, W. K. Norton, died in his room after taking poison capsules. Throughout the 1940s and 1950s, more suicides at the Cecil occurred. In 2008, two long-time residents referred to the Cecil as "The Suicide", and it became a popular nickname in social media years later.
        
        <br><br>Cecil became a notorious rendezvous spot for adulterous couples, drug activity, and a common ground for prostitutes.
        
        <br><br>In 1964, a retired telephone operator named "Pigeon Goldie" Osgood who had been a well-known and well-liked long-term resident at the hotel was found dead in her room. She had been raped, stabbed, beaten and her room ransacked. Jacques B. Ehlinger was charged with Osgood's murder because he was seen covered in blood roaming the streets close to the hotel, but was later cleared as a suspect. Her murder remains unsolved.
    
