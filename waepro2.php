    <h1>Amdromalius</h1>
<h3>Origin:</h3>
<p>
Before his fall, Andromalius was a celestial angel tasked with maintaining balance and justice. However, witnessing the constant injustices and imbalances frustrated him greatly. This irritation led him to take extreme measures to correct what he perceived as flaws in the celestial system. His radicalism and the creation of relentless vengeance against those he deemed guilty earned him his exile, being cast into the infernal abyss.
</p>
<br>

<h3>Characteristics:</h3>
<p>
Andromalius appears before those who summon him as an imposing man with a large serpent coiled around his arm, symbolizing his judgment and punishment abilities. His most terrifying aspect emerges when his eyes turn completely black, reflecting the souls he has judged and condemned. He prefers to manifest in dark and cold places, where the shadow of his being and the coldness of his heart can be felt more intensely.
</p>
<br>
<h3>Dominion:</h3>
<p>
This demon has power over judgment and vengeance. His supernatural ability is to detect evil, betrayal, and lies. He can also locate stolen objects and uncover hidden treasures. He holds particular influence over the human conscience, filling those who act unjustly with remorse and guilt.
</p>
<br>
<h3>Legions:</h3>
<p>
Andromalius commands 36 legions of infernal spirits, each specialized in a form of vengeance or punishment. His influence spans both the underworld and the earthly plane, where his legions lurk, ready to execute their twisted justice.
</p>
<br>
<h3>Prophecies:</h3>
<p>
Rumors suggest that, in a time of great corruption and malice on Earth, Andromalius will rise as the ultimate judge. Under his reign, all injustices will be severely punished, and only the truly pure will be able to escape his judgment.
</p>
<br>
<h3>Invocation:</h3>
<p>
Invoking Andromalius promises justice and vengeance. Those seeking to recover what has been stolen from them or expose a traitor can turn to him. To invoke him, his symbol must be drawn on the ground, alongside three black candles. The prayer to recite is:

''Guardian of justice, avenger of the betrayed, I invoke your judgment, Andromalius. Make justice prevail, and let the unjust pay their price''.

In exchange for his favor, the summoner must offer a promise to always act with integrity and justice.
</p>
<br>
<h3>Protection:</h3>
<p>
Although Andromalius can offer justice, his form of justice is relentless and often ruthless. Those who invoke him must be prepared to face their own judgment. To protect oneself from him, it is recommended to carry a tiger's eye amulet and recite protection prayers every night. It is also vital to always act with fairness and honesty, as any deviation from these principles can attract his wrath.
</p>
