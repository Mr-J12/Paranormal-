    <h1>Sallos</h1>
  <h3>  Origin</h3>
  <p>
Sallos, also known as Saleos or Zaleos, once served in the celestial ranks as an angel tasked with overseeing the bonds of love and devotion. However, he became captivated by earthly passions and began to tamper with these connections, forging unholy loves and obsessions. His actions triggered tumultuous relationships, causing conflicts on Earth and in Heaven. When discovered, he was banished and fell into hell, taking with him his knowledge of human hearts.
</p>
<br>

<h3>Characteristics:</h3>
<p>
Sallos possesses the unique ability to manipulate feelings of love and desire. Despite his nature, he manifests peacefully: a gallant soldier riding a crocodile with a ducal crown. However, the look in his eyes reveals the depths of his power, and the crocodile symbolizes the hidden passions that can devour a human being. Although he may appear tranquil, his presence always carries an underlying tension.
</p>
<br>
<h3>Dominion:</h3>
<p>
Sallos rules over the most intimate emotions of human beings, especially love and desire. He can ignite or extinguish passions at will, turning platonic loves into dangerous obsessions or transforming true love into cold indifference. He also has the ability to alter perception, making people see reality distorted by their emotions.
</p>
<br>
<h3>Legions:</h3>
<p>
Sallos commands 30 legions of spirits, each specialized in a different form of love or desire. His dominion is a palace in hell, where serving spirits create illusions of mortals' deepest desires and fears.
</p>
<br>
<h3>Prophecies:</h3>
<p>
It is rumored that Sallos has a plan to unleash an event known as the ''Twilight of Passions,'' where human emotions will reach their peak, leading humanity into chaos of loves, betrayals, and conflicts. It is said that during this time, the line between love and hate will be so fine that it will become indistinguishable.
</p>
<br>
<h3>Invocation:</h3>
<p>
Summoning Sallos promises control over the feelings and perceptions of others. To do so, the summoner must draw his symbol on the ground. The prayer to recite is:

''Lord of deep passions, shaper of the desires of the heart, Sallos, I call upon you. Entwine the threads of love and obsession for me''.

The summoner must offer a personal love object, such as a letter or a gift from a loved one, and burn it as an offering to Sallos.
</p>
<br>
<h3>Protection:</h3>
<p>
However, manipulating emotions comes at a price. Those who invoke Sallos may find themselves ensnared in a web of contradictory emotions, unable to distinguish between genuine love and fleeting passions. To protect oneself from his influence, it is recommended to wear a rose quartz amulet, a crystal known for its protective properties against negative emotions. To expel Sallos, a purification ritual with rosewater and sea salt must be performed while reciting a chant of protection for pure and true love.
</p>
