    <h1>Bathin</h1>
<h3>Origin:</h3>
<p>
Bathin, also known as Bathym, Mathim, and Marthim, was once a guardian angel of the celestial realms. This angel, enchanted by the power of earthly gems and herbs, made the grave mistake of attempting to combine his angelic essence with earthly magic. In doing so, he unleashed a storm of energy that resulted in a celestial cataclysm. Upon witnessing his actions, the archangels condemned him, forcing him to fall from the firmament and become an abomination between angel and beast.
</p>
<br>

<h3>Characteristics:</h3>
<p>
Bathin possesses the unique ability to understand the hidden virtues of gemstones and herbs, granting him powers of healing, cursing, and transmutation. He manifests as a robust man with the tail of a snake, his eyes shining with a mixture of disdain and wisdom. When he chooses to frighten, he can appear atop a pale horse, symbolizing death and oblivion. Most terrifying is his ability to transport human souls from one country to another, tearing them from their reality and carrying them to unknown dimensions.
</p>
<br>
<h3>Dominion:</h3>
<p>
Bathin rules over the greed and desire for power that gemstones and herbs can bestow. He manipulates the elements, turning water into fire and air into earth. He can influence the human mind, whispering promises of wealth and power, leading his victims to acts of betrayal and greed.

</p>
<h3>Legions:</h3>
<p>
He governs over 30 legions of spirits, each specializing in different gemstones and herbs. His realm is located in a corner of Hell, a vast cavern filled with shining gemstones and forests of cursed herbs. Here, trapped souls forever seek the gems that promised riches but brought only damnation.
</p>
<br>
<h3>Prophecies:</h3>
<p>
It is said that, in the twilight of times, Bathin will unleash a rain of cursed gems upon the Earth. Those who touch them will be consumed by insatiable greed, leading the world into an era of betrayal and chaos.
</p>
<br>
<h3>Invocation:</h3>
<p>
Invoking Bathin promises immeasurable wealth and hidden knowledge. To call upon him, the summoner must draw his symbol. The prayer to recite is:

''Guardian of the gem and brew, I invoke your essence. Bathin, he who fell due to his desire, I offer my loyalty in exchange for your wisdom and power''.

The summoner must offer a pure gem, preferably not acquired, and a rare herb, burned as an offering.
</p>
<br>
<h3>Protection:</h3>
<p>
Once Bathin is invoked, the summoner will be bound to him, their soul marked by greed. Over time, wealth and power will become unsatisfying, leading to an endless quest for more. To protect oneself, one must carry a silver amulet blessed by a being of light and regularly sprinkle their abode with holy water mixed with salt and ash. To expel Bathin, a purification ritual with incense and sacred chants is necessary, ensuring that the summoner is free from all greed before beginning.
</p>
