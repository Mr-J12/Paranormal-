    <h1>Aim</h1>
<h3>Origin:</h3>
<p>
Aim, also known as Aym or Haborym, in ancient times was a celestial duke tasked with preserving beauty and harmony in the universe. However, his fascination with the power of fire, an element as destructive as it is creative, led him down a dark path. In his obsession, he created flames that consumed entire heavens, hoping to find superior beauty within them. His rebellion reached its peak when he attempted to redefine the cosmic order, resulting in his downfall and banishment to the abyss.
</p>
<br>

<h3>Characteristics:</h3>
<p>
Aim manifests as a beautiful figure with a human appearance, but it is his triad of heads that paralyzes those who behold him: a serpent head symbolizing deceit, a human head with two stars on the forehead representing his former celestial rank, and a calf's head evoking ancient sacrifices. His arrival is always heralded by a trail of fire, and he rides a viper as a display of his mastery over the venomous. Aim possesses the power to ignite cities and fortresses, but he can also inspire wit in those who seek it.
</p>
<br>
<h3>Dominion:</h3>
<p>
Aim reigns over fire and destruction, but also over inspiration and creation through art and engineering. His influence is felt in moments of great ingenuity or in the most devastating tragedies caused by fire.
</p>
<br>
<h3>Legions:</h3>
<p>
This great duke of Hell commands 26 legions of lesser spirits, each specialized in different forms of fire, from ethereal blue flames to the most scorching conflagrations. These entities venerate him and spread chaos in his name.
</p>
<br>
<h3>Prophecies:</h3>
<p>
It is said that in the final days, Aim will unleash a fire that will not only consume the material world but also souls, purifying them for a new beginning. It is a fire that some believe will bring renewal, but others fear it will only bring annihilation.
</p>
<br>
<h3>Invocation:</h3>
<p>
Invoking Aim can grant mortals unprecedented wit. To call upon him, the summoner must draw his symbol in a circle of ashes and light three candles: black, red, and white. The prayer is:

''Lord of ancient flames, Aim, I summon you to bestow upon me the power of your wit. May the fire of your wisdom burn in my mind''.

In return, Aim may demand a fiery offering, such as a work of art that must be consumed by the flames.
</p>
<br>
<h3>Protection:</h3>
<p>
Aim's gift is not without cost. Those who invoke him may find their wit becoming an obsession, consuming them from within. To shield oneself from his fiery influence, it is advisable to carry an amulet of holy water or a blue quartz crystal. If it becomes necessary to banish him or counteract his influence, immersing oneself in a ritual bath with salt and purifying herbs can help break his bond.
</p>
