    <h1>Andras</h1>
<h3>Origin:</h3>
<p>
In the ancient times of the cosmos, when the heavens were not yet dominated by divine rules, Andras was once a guardian of the celestial realm. His angelic figure was a symbol of purity. But a forbidden love with a shadowy entity consumed him, leading him to question the justice of celestial laws. His rebellion against the gods was fierce, but it ended with his fall, transformed into a cursed being and stripped of his divine grace.
</p>
<br>

<h3>Characteristics:</h3>
<p>
Andras possesses the ability to sow discord, capable of igniting the flame of enmity even among the closest. He appears before mortals as a dark angel with a crow's head, riding a night wolf. However, his most terrifying manifestation is his sharply gleaming sword, ready to punish those who disobey him. It is said that when the air turns cold and a foul odor pervades everything, Andras is near.
</p>
<br>
<h3>Dominion:</h3>
<p>
Andras rules over quarrels, resentments, and divisions. His influence extends to the human mind, where he can kindle the darkest passions and deepest fears. It is rumored that he has the power to control the elements, especially fire and wind, and can cause storms and droughts at will.
</p>
<br>
<h3>Legions:</h3>
<p>
He reigns over 30 legions of lesser spirits, each more fearsome than the last. These entities serve Andras and fulfill his darkest desires. It is also said that he has dominion over an infernal region where condemned souls suffer under his command.
</p>
<br>
<h3>Prophecies:</h3>
<p>
Ancient scriptures speak of a time when Andras will join forces with other dark entities to unleash chaos on Earth. A war between light and darkness will be waged, and only those who bear a pure heart will be able to resist.
</p>
<br>
<h3>Invocation:</h3>
<p>
Invoking Andras promises power and mastery over enemies. To summon him, his symbol must be drawn, and one must recite:

''Andras, dark guardian of discord, I invoke you. Appear before me and bestow your power. I am willing to offer my soul in exchange for your grace and protection''.

In return, the summoner must sacrifice a cherished memory and offer the demon an article of a loved one.
</p>
<br>
<h3>Protection:</h3>
<p>
Invoking Andras is not an act to be taken lightly. Those who do so and do not fulfill their part of the bargain may find themselves in an eternity of torment at the hands of his legions. To protect oneself from him, one must carry a silver amulet with the image of an angel and recite a protection spell daily at dawn. If someone feels threatened by his presence, throwing salt and reciting a prayer to the archangel Michael can expel him.
</p>
