    <h2>Compliance</h2><br>
    <p>In the early part of the last decade, over 70 phone calls were made to supermarkets and fast food restaurants across the US. The MO was always the same: the caller would claim to be a police officer, allege that an employee was guilty of theft, and request her colleagues to carry out a strip search. The manager on duty would comply. After all, this was a cop calling. But on at least one occasion – in the Mount Washington, Kentucky branch of McDonald’s in 2004 – the caller’s demands went much further. ‘Compliance’ dramatises these events in clear, clinical detail, painting a stark, devastating portrait of human susceptibility in the face of an unseen authority.

        <br><br>Dreama Walker plays Becky, the young checkout girl at a ‘Chickwich’ franchise, who puts up scant resistance when store manager Sandra (Ann Dowd) calls her into the back office on the orders of Officer Daniels (Pat Healy) – who is actually a quiet suburban father hundreds of miles away. The chain of events that unfold strain believability, but writer-director Craig Zobel’s script hews disturbingly close to the facts.
        
        <br><br>This approach doesn’t entirely work: although the overall mood of deepening moral compromise is compellingly sustained, Zobel does struggle to sell some of the later scenes. Part of the problem is the casting: while Dowd and Healy are flawless, Walker seems a little too headstrong as the abused Becky.
        
        <br><br>But this doesn’t stop ‘Compliance’ from being a riveting, horrifying film, shot through with beautifully observed moments of unwelcome truth. It’s as much a critique of the enclosed systems of modern life – small towns, local authorities, dead-end jobs with meaningless heirarchies – as it is of sick individuals with cellphones.</p>
