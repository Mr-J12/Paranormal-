    <h1>Halphas</h1>
<h3>Origin:</h3>
<p>
Gusion, once a luminous being in the corridors of heaven, was the guardian of celestial secrets. His gift was to see beyond the veil of time. However, his insatiable curiosity led him to scrutinize forbidden destinies and reveal secrets that were meant to remain hidden. His rebellion was not violent but subtle, and it was that very subtlety that caused his fall from grace and descent into the underworld.
</p>
<br>

<h3>Characteristics:</h3>
<p>
Appearing as a Xenopilus, a fearsome mythological creature with eyes reflecting the eternal flow of time, Gusion can tell all about the Past, Present, and Future. His voice is a distant echo, resonating with unfathomable truths. He prefers to manifest at moments of crucial decision when destinies are at stake.
</p>
<br>
<h3>Dominion:</h3>
<p>
Gusion rules over the perception of time and decisions. Those trapped in dilemmas or tormented by the past often feel his lurking presence. He has the power to manipulate memories and future visions, as well as to influence decisions made in the present.
</p>
<br>
<h3>Legions:</h3>
<p>
Commanding over 40 legions of spirits, these beings are like shadows, intangible and ever-shifting, representing fragments of time and destiny. Gusion has a throne in a specific realm of the underworld where rivers flow backward, and the sands of time move unpredictably.
</p>
<br>
<h3>Prophecies:</h3>
<p>
There are tales of an era when Gusion will release all knowledge of time, plunging the world into chaos where past, present, and future intertwine. Decisions will no longer hold meaning, and humanity will be trapped in an eternal cycle of doubt.
</p>
<br>
<h3>Invocation:</h3>
<p>
Those who seek Gusion's counsel will find answers to unresolved questions and clarity in their decisions. To invoke him, one must draw his special symbol on an ancient mirror and recite:

''Guardian of lost times, reveal what has been, what is, and what will be''.

In return, the summoner must sacrifice a precious memory that Gusion will absorb, leaving a void in its place.
</p>
<br>
<h3>Protection:</h3>
<p>
The clarity that Gusion offers can also ensnare souls in time loops, endlessly reliving their worst decisions or fears. To protect oneself, one should carry an amulet of phoenix tear, a rare stone immune to temporal manipulations. If the need arises to expel Gusion, reciting acceptance mantras and living in the present can weaken his power over the individual.
</p>
