    <div class=".anton-regular"><h2>Zodiac</h2></div><br>
    <p>In ‘Zodiac’, the serial killer is a setter of engrossing puzzles, a bit like a crossword compiler, or a certain type of film director. David Fincher’s movies have always had this flavour: ‘The Game’, ‘Panic Room’, ‘Fight Club’, ‘Alien3’ are all cat-and-mouse affairs of one kind or another; ‘Se7en’ proposed multiple murder as literary pop quiz. ‘Zodiac’ isn’t a puzzle film in quite that way; instead its subject is the compulsion to solve puzzles, and its coup is the creeping recognition, quite contrary to the flow of crime cinema, of how fruitless that compulsion can be.

        <br>Stretching from 1969 to 1991, the film is based on the series of killings that petrified San Francisco during the ’70s, and more specifically on the book written about them by Robert Graysmith.<br> Played by a typically puppyish Jake Gyllenhaal, Graysmith was a San Francisco Chronicle cartoonist whose fascination with the murders engendered a kind of partnership with foppishly dissolute crime editor Paul Avery (a wrapped gift of a part for Robert Downey Jr), mirrored in the investigations of police detectives Mark Ruffalo and Anthony Edwards.
        
        <br>Portraying the Zodiac’s attack on a young couple in a car in a deserted make-out spot, the opening scene – one of several bravura suspense sequences – can’t help but recall slasher convention; <br> the investigation that follows seems like a pacey procedural. But the clues don’t quite fit together, the solution doesn’t come, and as procedure sprawls into obsession the tone shifts from genre picture to something more like curious observation – sometimes sympathetic, sometimes almost mocking – of the refusal to let go. Perhaps some puzzles should be set aside.</p>
