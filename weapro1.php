    <h1>Amdusias</h1>
<h3>Origin:</h3>
<p>
Amdusias, originally the Angel of Heavenly Harmony, was responsible for the songs and melodies that resonated in the divine halls. His ambition, however, led him to desire to change the rhythm of the cosmos and subjugate the songs of other angels under his baton. His attempt to dominate the celestial concert led to his fall, being banished to the abyss, where his music took on a darker and more terrifying tone.
</p>
<br>

<h3>Characteristics:</h3>
<p>
In his initial manifestation, this demon appears as an imposing unicorn with dark fur and eyes dripping with melancholy. But upon request, he can assume a human form, exuding an aura of majesty. With a simple gesture, Amdusias can make trumpets and other instruments sound, creating an infernal cacophony that disturbs the soul. Moreover, he has the power to bend and move trees at his will, as if they were living serpents.
</p>
<br>
<h3>Dominion:</h3>
<p>
Amdusias masters the art of music, especially tones that influence human emotions and thoughts. His ability to control nature allows him to influence the environment, making forests respond to his infernal melody.
</p>
<br>
<h3>Legions:</h3>
<p>
He commands 29 legions of spirits, primarily composed of entities related to art, music, and nature. These spirits reflect his duality, capable of creating both beautiful and terrifying melodies.
</p>
<br>
<h3>Prophecies:</h3>
<p>
There is a myth that speaks of a great symphony that Amdusias is composing. Once completed, it will resound throughout the world, plunging humanity into a trance, and those who hear its call will be drawn into his infernal domain.
</p>
<br>
<h3>Invocation:</h3>
<p>
Those who invoke Amdusias can gain exceptional abilities in music and art. They will also be able to control aspects of nature with his assistance. To invoke him, his special symbol must be drawn in a forest during the new moon, and one must recite:

''Oh Amdusias, master of melody, share your dark symphony with me''.

In exchange, the summoner must offer an original melody, one that is both beautiful and melancholic, as a tribute to his greatness.
</p>
<br>
<h3>Protection:</h3>
<p>
Engaging with Amdusias is not without danger. His music can ensnare souls, leaving them eternally captive in his infernal domain. Those who wish to protect themselves from his influence must carry an amulet of silence, a small stone that absorbs sounds, ensuring that the demon's music does not reach their ears. If his influence is felt, one must seek stillness, moving away from any source of music until his power fades.
</p>
