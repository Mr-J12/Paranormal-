    <h1>Barbatos</h1>
<h3>Origin:</h3>
<p>
In ancient times, Barbatos was a master of the melodies and harmonies of nature, a divine being of the Order of Virtues. However, his obsession with the dark secrets of the earth and hidden voices led him to challenge celestial boundaries, resulting in his condemnation and transformation into a Grand Duke of the Underworld.
</p>
<br>

<h3>Characteristics:</h3>
<p>
His figure is more subtle than that of other demons. He often manifests accompanied by noble Kings and great troops. Nature resonates with his presence: bird songs, dog barks, whispers of the wind. However, when the Sun is in Sagittarius, his shadow takes on greater density, and his truly terrifying form can be glimpsed.
</p>
<br>
<h3>Dominion:</h3>
<p>
Barbatos rules over the voices and sounds of nature, bestowing upon those who invoke him an understanding of these hidden messages. Additionally, he has the power to break enchantments and uncover hidden treasures, as well as to influence human relationships, reconciling friendships and bringing those in power closer.
</p>
<br>
<h3>Legions:</h3>
<p>
Under his command are more than 30 legions of spirits, entities specialized in uncovering secrets and manipulating the energies of nature. His influence is felt especially in places where nature and the esoteric converge, such as ancient forests and mystical ruins.
</p>
<br>
<h3>Prophecies:</h3>
<p>
There is an ancient prophecy that says that in a total eclipse, Barbatos will release all the voices of nature in a single song, revealing secrets so profound that they could change the course of human history.
</p>
<br>
<h3>Invocation:</h3>
<p>
Those who invoke Barbatos gain a deep understanding of the voices of nature, discovering hidden secrets and treasures concealed by magic. Additionally, they can reconcile broken friendships and approach people in positions of power. One who seeks to invoke Barbatos must draw his symbol during the period when the Sun is in Sagittarius. While reciting,

''Barbatos, lord of hidden voices, reveal the secrets that nature hides,''

the summoner must offer him an object of great personal value, preferably something related to a secret or intimate memory.
</p>
<br>
<h3>Protection:</h3>
<p>
Invoking Barbatos is not without risk. Those who do so may be overwhelmed by the secrets revealed or become the target of manipulations by other entities. To protect oneself from him, it is advisable to carry an amulet made of oak and sea salt and recite protection mantras before dawn and after dusk. To expel him, the sound of sacred bells has proven to be effective.
</p>
