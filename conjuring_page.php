    <h2>The Conjuring (2013)</h2><br>
    <p>Getting a blood transfusion of molten steel is possibly the only way you’ll stay in your seat all the way through this haunted house movie. No blood is spilled, but American censors rated it R (equivalent to our 18) anyway – reportedly for being ‘too scary’. I’ll confess to letting out a yelp during one scene, like a puppy being strangled, as an invisible hand tugged at the leg of a sleeping child.

        <br><br>It’s directed by James Wan (the man who started the ‘Saw’ franchise) and claims to based on ‘true case files’. Set in 1971, Carolyn and Roger Perron (Lili Taylor and Ron Livingston) have just moved into a classic creepy old farmhouse: nooks, crannies and a barricaded basement. Clearly something is up when their dog refuses to enter the house. Plus the Perrons have too many teenage-ish daughters for comfort (girls of a certain age being lightning rods for malevolent spirits, as every horror fan knows).
        
        <br><br>Wan builds mounting dread with silence and suspense, lingering the camera unsettlingly long here, creaking a door there. Finally, after a night from hell, the Perrons seek professional help. And if your house is haunted, who you gonna call? Lorraine and Ed Warren – the husband and wife ghostbusters (played here by Vera Farmiga and Patrick Wilson) who de-spooked the house in Amityville. The plot is skeletal. And ‘The Conjuring’ is not ingenious like, say, the first ‘Paranormal Activity’.
        
        <br><br>One reason it works is the acting. Lili Taylor is totally convincing as scared mum Carolyn. And heaven only knows how the filmmakers got a look of pure terror on the face of 12-year-old actress Joey King (maybe they dangled Justin Bieber over a balcony in front of her). Best of all, the script doesn’t take itself too seriously, adding in stupid-funny lines. Wan is going great guns right up until he unveils the source of the evil, and with it unleashes prosthetics and predictable horror movie clichés (he did the same in ‘Insidious’). But by this time you’ll have had the sweet bejesus spooked out of you and may not care.</p>
