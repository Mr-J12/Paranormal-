    <h1>Andrealphus</h1>
<h3>Origin:</h3>
<p>
Andrealphus, originally a celestial master of the stars and divine geometry, was cast out for his desire to bestow celestial knowledge upon mortals. His eagerness to defy celestial laws and his arrogance in believing he could elevate humans to the status of gods led to his downfall.
</p>
<br>

<h3>Characteristics:</h3>
<p>
This Marquis of the Abyss initially manifests as a terrifyingly beautiful peacock, its striking colors masking the diabolical essence behind its feathers. However, it can assume a human form, a distorted reflection of its former celestial glory. Through him, geometry and astronomy become as clear as crystal for any mortal.
</p>
<br>
<h3>Dominion:</h3>
<p>
Andrealphus rules over the exact sciences, particularly those that explore the structure of the universe. His touch can make a man understand the constellations and the complexities of space, or transform a human into the likeness of a bird, granting them a bird's-eye view.
</p>
<br>
<h3>Legions:</h3>
<p>
He commands 30 legions of infernal spirits, many of whom were ancient scholars and astronomers seeking wisdom and knowledge beyond human limits. These spirits specialize in influencing scientists and mathematicians in the mortal world.
</p>
<br>
<h3>Prophecies:</h3>
<p>
There is an ancient prophecy suggesting that when the stars form a specific constellation, Andrealphus will attempt to build a bridge between Hell and Heaven, using sacred geometry and the power of his followers.
</p>
<br>
<h3>Invocation:</h3>
<p>
The summoner seeking Andrealphus's favor will gain profound knowledge of the universe and skills in geometry and astronomy. To invoke him, his special symbol must be drawn on the ground, and the prayer must be recited:

''Andrealphus, master of the stars, reveal the secrets of the universe and transform my mind''.

In return, the summoner must offer a masterpiece of knowledge or a measuring instrument they have used in their studies.
</p>
<br>
<h3>Protection:</h3>
<p>
The relationship with Andrealphus comes at a price. Those who delve too deeply into the secrets he reveals may become lost in the depths of knowledge, going mad or becoming obsessed. It is essential to draw a circle of salt around the symbol for protection. If one feels his influence is too powerful, breaking a compass and scattering its fragments can sever the connection with the demon.
</p>
