    <h2>The Enfield Monster</h2><br>
    <p>At about 10:00pm on the night of April 25, 1973, Henry McDaniel heard a scratching sound at his front door. He looked out and saw something that he thought might be a bear. Taking a gun and flashlight, he headed outside into a strong wind and saw a creature between two rosebushes.[3] He later said "It had three legs on it, a short body, two little short arms, and two pink eyes as big as flashlights. It stood four and a half feet tall and was grayish-colored."[4] He added later that it was "almost like a human body".[4]

        <br><br>McDaniel fired four shots at the creature, one shot hitting it and causing it to make a hiss "much like a wildcat's", before fleeing towards a nearby railway embankment, covering 50 feet in three jumps.[4] McDaniel called the local authorities who discovered footprints in the soft earth near the house, which McDaniel described as dog-like in shape, with six toe pads. The police considered McDaniel to be "rational and sober" in his reporting of the incident.[4] In a later press interview, McDaniel said "If they do find it, they will find more than one and they won't be from this planet, I can tell you that."[4]
        
        <br><br>Investigators interviewing nearby residents were told that Greg Garrett, a 10-year-old neighbor of McDaniel, claimed to have encountered the creature half an hour before McDaniel did, and that the creature had stepped on his feet, tearing his tennis shoes to shreds. The boy later told Western Illinois University researchers that his report was a hoax "to tease Mr. M and have fun with an out of town newsman."[1][2]
        
        <br><br>Two weeks later on May 6, McDaniel called the radio station WWKI claiming to have seen the creature again, at 3 a.m. that morning.[2] It was negotiating the trestles of the railroad tracks near his home, and McDaniel said "I saw something moving out on the railroad track and there it stood. I didn't shoot at it or anything. It started on down the railroad track. It wasn't in a hurry or anything."[citation needed] A search party including WWKI's news director Rick Rainbow[3] explored the area later that day, and reported observing an "apelike"[2] creature standing in an abandoned building near McDaniel's house.[2] They claimed to have made a recording of the creature's cries, and fired a shot at it before it fled.[2] Cryptozoologist Loren Coleman investigated the case and the sound recording.[5]
        
        <br><br>Two days later, a day after McDaniel was interviewed on local radio, the local press reported that police were called to investigate reports of gunfire and arrested five young men from out of town who had come to Enfield in order to photograph the creature, carrying shotguns and rifles "for protection", the men having claimed to have sighted the creature. The White County sheriff dismissed reports of this "monster hunting expedition" as an exaggeration, saying that the men were just "out drinking and raising hell", mentioning the monster only briefly during questioning.[1] The men were charged with hunting violations.</p>
