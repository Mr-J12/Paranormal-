    <h1>Gremory</h1>
<h3>Origin:</h3>
<p>
Before her fall, Gremory, known in heaven as Gamoriel, was one of the most radiant guardians of celestial treasures. But her insatiable desire for love and admiration led her to weave forbidden spells. She used her magic to enchant and seduce angels and divine beings, distorting their genuine love into an obsession. This abuse of power caused her exile, transforming her into the demon Gremory.
</p>
<br>

<h3>Characteristics:</h3>
<p>
Gremory manifests as a woman of supernatural beauty, mounted on an imposing camel, wearing a crown that symbolizes her dominion. Her eyes hold secrets of past and future treasures and loves. Although she can be charming, her true form, revealed to those who betray her, is terrifyingly seductive.
</p>
<br>
<h3>Dominion:</h3>
<p>
She reigns over hidden secrets, especially buried treasures and unrequited loves. She possesses the supernatural ability to unearth what has been concealed and can influence the human heart, provoking intense feelings of love and desire.
</p>
<br>
<h3>Legions:</h3>
<p>
She rules over 26 legions of spirits, each specializing in a form of seduction or enchantment. These spirits roam the world, seeking lonely souls to charm and draw towards Gremory.
</p>
<br>
<h3>Prophecies:</h3>
<p>
It is said that one day, Gremory will attempt to use her power to create a world where love and desire reign without restraint, where treasures remain uncovered, and all are under her enchantment.
</p>
<br>
<h3>Invocation:</h3>
<p>
Invoking Gremory grants knowledge of hidden treasures and the power of irresistible seduction. To summon her, one must draw her special symbol on a reflective surface under the light of the full moon, surrounded by black roses. The summoner must recite:

''Gremory, queen of hidden desires, reveal the concealed secrets and make me an object of desire''.

In exchange, the summoner must offer a personal item infused with their essence.
</p>
<br>
<h3>Protection:</h3>
<p>
Although the invocation can bestow riches and seductive powers, the price is high. Those who invoke Gremory often find themselves ensnared in a web of desires and obsessions. To protect oneself, one should wear a pure silver amulet and a fragment of a broken mirror. In the event of needing to expel her, holy water sprinkled over the summoning symbol and reciting a purification prayer can help break her enchantment.
</p>
