    <h2>The Axe Murder House</h2>
    <p>The Moore family consisted of parents Josiah B. (aged 43), Sarah (n√©e Montgomery) (39), and their four children: Herman Montgomery (11), Mary Katherine (10), Arthur Boyd (7), and Paul Vernon (5). An affluent family, the Moores were well known and well liked in their community. On June 9, 1912, Mary Katherine Moore invited Ina Mae (8) and Lena Gertrude Stillinger (12) to spend the night at the Moore residence. That evening, the visiting girls and the Moore family attended the Presbyterian church where they participated in the Children's Day Program, which Sarah had coordinated. After the program ended at 9:30 p.m., the Moores and the Stillinger sisters walked to the Moores' house, arriving between 9:45 and 10 p.m.

        <br><br>Discovery of the crime
        <br><br>At 7 A.M. the next day, June 10, Mary Peckham, the Moores' neighbor, became concerned after she noticed that the family had not come out to do their morning chores. Peckham knocked on the Moores' door. When nobody answered, she tried to open the door and discovered that it was locked. Peckham let the Moores' chickens out and called Ross Moore, Josiah's brother. Like Peckham, Moore received no response when he knocked on the door and shouted. Ross unlocked the front door with his copy of the house key. While Peckham stood on the porch, Ross went into the parlor and opened the guest bedroom door, where he found Ina and Lena Stillinger's bodies on the bed. Moore immediately told Peckham to call Henry "Hank" Horton, Villisca's primary peace officer, who arrived shortly thereafter. Horton's search of the house revealed that the entire Moore family and the two Stillinger girls had been bludgeoned to death. The murder weapon, an axe belonging to Josiah, was found in the guest room where the Stillinger sisters were found.
        
        <br><br>Doctors concluded that the murders had taken place between midnight and 5 a.m. Two spent cigarettes in the attic suggested that the killer or killers patiently waited (according to the official tour, there were no cigarettes found) in the attic until the Moore family and the Stillinger guests were asleep. The killer(s) began in the master bedroom, where Josiah and Sarah Moore were sleeping. Josiah received more blows from the axe than any other victim; his face had been cut to such an extent that his eyes were missing. The ceiling in his room also had a gouge mark from when the murderer lifted the axe to murder him.[4]
        
        <br><br>The killer or killers used the blade of the axe on Sarah, while using the blunt end on the rest of the victims. Herman, Mary Katherine, Arthur, and Paul were next bludgeoned in the head in the same manner as their parents. Afterwards, the murderer returned to the master bedroom to inflict more blows on the elder Moores, knocking over a shoe that had filled with blood, before moving downstairs to the guest bedroom and killing Ina and Lena. Seemingly afterwards, a 4lb slab of bacon was taken out of the icebox and laid next to the axe. Investigators also found untouched food and bloody water during the search. After the search, people were let in to see if they could have committed the crime, completely contaminating the weapon.
        
        <br><br>Investigators believed that all of the victims except for Lena Stillinger had been asleep when murdered. They thought that she was awake and tried to fight back, as she was found lying crosswise on the bed, and with a defensive wound on her arm. Lena's nightgown was pushed up to her waist and she was wearing no undergarments, leading to law enforcement speculation that the killer(s) sexually molested her or attempted to do so. This was later proven false.</p>
