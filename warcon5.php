    <h1>Leraje</h1>
<h3>Origin:</h3>
<p>
Before his fall, Leraje, also known as Leraikha, was the guardian of justice, a celestial archer whose mission was to maintain harmony and balance in the celestial realms. However, his passion for perfection led him to challenge the decisions of higher beings, deeming them unjust. By defying the established order with his arrows of justice, he was banished, becoming the master of conflict and discord.
</p>
<br>

<h3>Characteristics:</h3>
<p>
Leraje manifests as a spectral archer, dressed in shades of green, his gaze always fixed on a distant target. His quiver overflows with cursed arrows, capable of causing wounds that fester and decay. Not only can he influence battles and contests, but his arrows, once they find a target, sow conflict and discord wherever they go.
</p>
<br>
<h3>Domain:</h3>
<p>
Leraje rules over the realm of conflicts, from simple disagreements to battles of epic proportions. His influence is particularly palpable in soul wounds, those personal disputes that refuse to heal. He also has a particular control over physical wounds caused by archers, making them especially difficult to cure.
</p>
<br>
<h3>Legions:</h3>
<p>
He rules over 30 legions of spirits, each specialized in a form of discord. These spirits roam the world, seeking conflicts to amplify and wounds to widen.
</p>
<br>
<h3>Prophecies:</h3>
<p>
There is a legend that speaks of a day when Leraje will unleash a rain of arrows upon the world, inflaming every heart with anger and resentment, pushing humanity to the brink of its own annihilation.
</p>
<br>
<h3>Invocation:</h3>
<p>
Those who seek to invoke Leraje usually desire to sow chaos or control the course of a conflict. To invoke him, one must trace his special symbol on the ground, surrounded by laurel leaves, and recite:

''Leraikha, lord of conflict, guide my arrow and my cause''.

In return, the summoner must offer a personal memory of a past conflict, thus feeding Leraje's power.
</p>
<br>
<h3>Protection:</h3>
<p>
Summoning Leraje is not without risks. While he can grant control over a conflict, the summoner may find themselves trapped in an endless cycle of disputes and animosities. To protect oneself from his influence, it is recommended to wear an amulet made of olive branches, a symbol of peace. In extreme cases, a ritual circle surrounded by salt and lavender can act as a barrier against his arrows of discord
</p>
