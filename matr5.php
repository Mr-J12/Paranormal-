    <h1>Foras</h1>
<h3>Origin:</h3>
<p>
Foras, originally a being from the celestial realm tasked with the custody of the knowledge of nature, was consumed by his insatiable ambition. His obsession with logic and ethics led him to question divine order and the designs of the supreme. This heresy plunged him from the celestial heights into the abyssal depths of the underworld.
</p>
<br>

<h3>Characteristics:</h3>
<p>
This demon, though he has a preferred form of a strong and robust man, can assume any human form, making him even more dangerous and elusive. He is a master of the arts of logic and ethics and possesses profound knowledge of the mystical properties of herbs and gemstones. But perhaps his most feared ability is his power to bestow invisibility and prolong the lives of those who invoke him.
</p>
<br>
<h3>Dominion:</h3>
<p>
Foras reigns over hidden wisdom and deep understanding of earthly virtues, from minerals to plants. With his intervention, one can discover hidden treasures, recover lost objects, and be endowed with the gift of eloquence.
</p>
<br>
<h3>Legions:</h3>
<p>
With command over 29 legions of spirits, the entities under his control work day and night to unearth secrets and knowledge. While no specific realm is known to be under his rule, his influence is palpable in places where knowledge and logic prevail.
</p>
<br>
<h3>Prophecies:</h3>
<p>
There are rumors suggesting that Foras intends to use his vast knowledge to rewrite the world's destiny, placing logic and reason above everything, even human emotions, creating a cold and calculating world.
</p>
<br>
<h3>Invocation:</h3>
<p>
Those who seek Foras's favor yearn for knowledge, eloquence, or the revelation of hidden secrets. When invoking him, his symbol must be traced on a parchment made of lamb's skin, placing a gemstone upon it and reciting:

''Foras, Guardian of Hidden Knowledge, reveal your secrets and bestow your favor''.

In return, Foras may demand that the summoner offer knowledge possessed only by them or a never-before-revealed secret.
</p>
<br>
<h3>Protection:</h3>
<p>
The knowledge that Foras offers is not harmless. Those who invoke him may find themselves trapped in an endless quest for wisdom, losing all traces of humanity and emotion in the process. To protect oneself from him, it is recommended to carry a jade talisman, known for its protective properties against malevolent influences. If someone believes they are under his influence, a purification ritual with salt and sacred herbs should be performed, reciting protection mantras and seeking the favor of benevolent entities.
</p>
