    <h2>Poltergeist</h2><br>
    <p>Credited to Hooper, but every inch a Spielberg film, this is a barnstorming ghost story, set in one of the small suburban houses Spielberg knows and loves, where the family canary is called Tweety, and the kids read Captain America comics and eat at the Pizza Hut. <br><br> Gradually this impossibly safe world is (in a truly ingenious plot development) invaded by something inside the family television. Soon the plot takes off into a delirious fight with demonic forces suggestive of nothing so much as a Walt Disney horror movie; and although the sub-religious gobbledegook (including a tiresome midget medium) is hard to take. <br><br> it is consistently redeemed by its creator's dazzling sense of craft. For this one, Spielberg has even contrived a structural surprise which leaves the audience spinning like one of his house's haunted rooms, and arguably matches the opening of Psycho in its impudent virtuosity.</p>
