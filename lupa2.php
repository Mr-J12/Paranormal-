    <h1>Dantalion</h1>
<h3>Origin:</h3>
<p>
In the celestial spheres, Beleth, known for his angelic melodies, was the master of harmony and love. However, his obsession with merging both emotions led him to manipulate the passions of other divine beings, causing unprecedented chaos in the ethereal realm. His attempt to control the emotions of higher beings was seen as audacious, condemning him to be banished, and thus, the once angel of melody transformed into a demon of passion.
</p>
<br>

<h3>Characteristics:</h3>
<p>
Beleth appears on a pale horse, followed by a deafening symphony of trumpets and other instruments, heralding his arrival. Although his initial fury is fearsome, his true power lies in his ability to influence love and passion. He can make the hearts of mortals and demons ignite with an irresistible desire, manipulating their feelings at will.
</p>
<br>
<h3>Dominion:</h3>
<p>
He rules over love, passion, and obsession. His influence can be so powerful that he can make two souls entwine eternally or repel each other with the same intensity. His presence is often felt in doomed romances, forbidden passions, and obsessive loves.
</p>
<br>
<h3>Legions:</h3>
<p>
Beleth commands 85 legions of spirits, many of whom once served under his baton in the celestial realm, creating divine harmonies. These spirits, now fallen, seek to fulfill their desires in hell and on Earth, spreading love and obsession in the name of their master.
</p>
<br>
<h3>Prophecies:</h3>
<p>
It is said that one day Beleth will unleash a passion so powerful that it will consume the world, leading humanity to its own destruction through love and desire.
</p>
<br>
<h3>Invocation:</h3>
<p>
Summoning Beleth grants the power of love and passion over others. To call upon him, one must trace his special symbol within a triangle, strike with a hazel wand to the south and east, and recite:

''Great Beleth, king of passion, I submit my heart before your power''.

The summoner must offer a melody, a song that has marked their life, as tribute.
</p>
<br>
<h3>Invocation:</h3>
<p>
Dealing with Beleth is not straightforward. The passions he unleashes are often overwhelming, leading to madness or despair. Protecting oneself from his influence requires a pure heart, free from obsessions. Carrying a rose quartz amulet and singing hymns of true love can act as a shield. To expel Beleth, it is necessary to break the emotional bond he has created, seeking inner peace and releasing the heart from unwanted passions.
</p>
