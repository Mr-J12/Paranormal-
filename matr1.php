    <h1>Amy</h1>
<h3>Origin:</h3>
<p>
Amy, also known as Avnas, was once the celestial guardian of ancient wisdom and constellations. However, in his desire to share his vast knowledge with mortals without the permission of the divine realm, he incurred the wrath of the heavens. Deemed too dangerous, for fear that he could alter the fate written in the stars, he was banished, becoming a fiery entity, a symbol of his burning passion for knowledge.
</p>
<br>

<h3>Characteristics:</h3>
<p>
Amy initially manifests as uncontrolled fire, a whirlwind of flames that can consume everything in its path. However, over time, he assumes the form of a man, his human appearance concealing an inner fire. He is a master of astrology and all liberal sciences, capable of foreseeing destiny and altering the course of events.
</p>
<br>
<h3>Dominion:</h3>
<p>
Amy rules over the vast field of knowledge and wisdom. His presence can be invoked to decipher cosmic mysteries or unearth hidden treasures protected by guardian spirits. Moreover, he has the ability to grant magical familiars to those who seek his guidance.
</p>
<br>
<h3>Legions:</h3>
<p>
He governs 36 legions of spirits, each specialized in a different aspect of wisdom and knowledge. From the dark arts to the sciences, these spirits are guardians of forgotten secrets and unrevealed prophecies.
</p>
<br>
<h3>Prophecies:</h3>
<p>
There is a myth that Amy, in his quest for redemption, will once again attempt to share forbidden knowledge with humanity, which could lead to an era of enlightenment or catastrophe, depending on how that knowledge is used.
</p>
<br>
<h3>Invocation:</h3>
<p>
By invoking Amy, one can access hidden knowledge, foresee the future, or obtain magical familiars. To summon him, his special symbol must be drawn on the ground with elm wood ashes, and the symbol must be surrounded by seven lit candles. As the flames dance, the summoner must recite:

''Amy, guardian of the flames of knowledge, reveal what is hidden and guide my path in the darkness''.

In return, Amy will demand a precious memory from the summoner, which will be consumed by the flames of forgetfulness.
</p>
