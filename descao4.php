    <h1>Flauros</h1>
<h3>Origin:</h3>
<p>
Flauros, also known by various names, was originally a guardian of ancient secrets in the celestial realm. His insatiable desire for knowledge led him to challenge divine beings by attempting to decipher the mystery of creation. Being unworthy of such wisdom, he was banished, carrying fragments of that forbidden knowledge with him.
</p>
<br>

<h3>Characteristics:</h3>
<p>
Flauros initially manifests as a powerful leopard but can assume a human form with fiery eyes that ignite the soul with just one look. His responses consist of truths about the past, present, and future, as long as he is treated with the respect he demands. He is a dual being, both wise and deceitful.
</p>
<br>
<h3>Domain:</h3>
<p>
This demon has vast dominion over time and reality. He can reveal truths from any moment in history or predict the future. Moreover, he has the ability to influence human perception, distorting reality at his whim.
</p>
<br>
<h3>Legions:</h3>
<p>
Flauros rules over 36 legions of spirits, entities that dwell between the cracks of time and space. Each of them, in the image of their leader, carries a fragment of an ancient mystery.
</p>
<br>
<h3>Prophecies:</h3>
<p>
Ancient myths tell of Flauros playing a key role in the final showdown between light and darkness, where truth and deception will be the primary weapons. It is said that during that time, those who seek the truth will have to confront him.
</p>
<br>
<h3>Invocation:</h3>
<p>
Those who invoke Flauros will be rewarded with hidden truths. To do so, a triangle must be drawn on the ground, and in its center, his symbol should be drawn. The prayer to recite is:

''Flauros, guardian of the hidden, reveal what lies in the shadows. In exchange, I offer respect and recognition of your immense wisdom''.

The invoker must offer a valuable memory, allowing it to be consumed by fire.
</p>
<br>
<h3>Protection:</h3>
<p>
Dealing with Flauros is dangerous. Without the protection of the triangle, he can deceive and betray. To shield oneself from his influence, it is essential to wear an amulet with the symbol of an hourglass. If Flauros attempts to deceive you, throwing laurel leaves into the fire and reciting a gratitude mantra to the universe can dissipate his presence.
</p>
