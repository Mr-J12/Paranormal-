    <h1>Focalor</h1>
    <h3>Origin:</h3>
<p>
Focalor, once a luminous being under the name Lumifur, was destined to be the guardian of celestial waters. However, his love for power consumed him. He defied divine authority by attempting to unleash storms in the Seven Seas of Heaven, with the desire to become their sole lord. For his rebellion, he was banished, but his fall was like lightning across the ocean, creating the first storm in the earthly world.
</p>
<br>

<h3>Characteristics:</h3>
<p>
This demon manifests as a man with Gryphon wings, a grotesque fusion of brute strength and majesty. His eyes are bottomless abysses, and when he unfurls his wings, the winds swirl as if they would tear apart reality itself. He has the power to control the winds and seas, capable of killing men by drowning them in the waters and toppling warships.
</p>
<br>
<h3>Dominion:</h3>
<p>
Focalor holds power over the most volatile elements: wind and water. He also has a sinister influence over human despair and hope, able to annihilate or give life to the dreams of men.
</p>
<br>
<h3>Legions:</h3>
<p>
He rules over 30 legions of spirits, entities formed of storms and gales, sea monsters, and succubi of despair. His influence is most strongly felt in areas where the power of the sea and sky converge, such as ports and peninsulas.
</p>
<br>
<h3>Prophecies:</h3>
<p>
Focalor lives in the hope and prophecy that he will return to the Seventh Throne after a thousand years. It is a myth that on the day of his return, a biblical-scale cataclysm will strike the seas of the world, drowning the lands in a new deluge.</p>
</p>
<br>
<h3>Invocation:</h3>
<p>
To invoke Focalor, the summoner must draw his symbol on the ground, inside a circle of sea salt, and recite the prayer:

''Oh Focalor, Duke of Storms, I invoke you. May your winds guide my path, may your waters cleanse my enemies''.

The summoner must offer a reliquary filled with water from the deepest sea and air from the highest mountain. In return, Focalor will grant dominion over certain elements and protection against one's enemies.
</p>
<br>
<h3>Protection:</h3>
<p>
Summoning Focalor is dangerous; protection against enemies can easily turn into uncontrollable vengeance that consumes all. To safeguard oneself, one must wear a jade amulet and sea salt, and if necessary, banish him by reciting ''Psalm 91'' while breaking the sigil parchment and burning it in a purifying fire.
</p>
