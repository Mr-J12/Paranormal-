    <h1>Bael</h1>

<h3>Origin:</h3>
<p>
Before known as Bael or Baal, the First Principal Demon was a human monarch from the East, renowned for his cruelty and boundless ambition. His obsession with power and immortality led him to forge a pact with the dark forces that resided beyond the shadows. Upon his death, his soul found no rest and was condemned, transforming into the leader of infernal powers.
</p>
<br>
<h3>Characteristics:</h3>
<p>
Bael, with an unsettling air of majesty, imparts to those bold enough the art of invisibility. When he manifests, he does so with a raspy yet articulate voice, sending shivers down the spine of those who hear it. He appears in various forms: a Cat with incandescent eyes, a Viscous-skinned Toad with a penetrating gaze, or a Man marked by millennia of torment. At times, he combines these three avatars into a grotesque and chilling entity.
</p>
<br>
<h3>Dominion:</h3>
<p>
Bael exerts his influence over betrayal, conspiracy, and secrets. His aura can disturb the human mind, leading his victims to unspeakable acts. Those under his influence might become entangled in dark plots or reveal secrets that should remain hidden.
</p>
<br>
<h3>Legions:</h3>
<p>
He commands 66 infernal legions, composed of souls betrayed and condemned by their own actions. From his throne in the East of the underworld, Bael extends his influence, dominating vast regions in both hell and certain hidden places on Earth.
</p>
<br>
<h3>Prophecies:</h3>
<p>
There are rumors that when the sun and the moon share the same sky, Bael will unleash his legions upon the Earth, bringing with them an era of revealed secrets and rampant betrayals. Historically, those who sought power through intrigue were said to be ''Children of Bael.''
</p>
<br>
<h3>Invocation:</h3>
<p>
The summoner seeking Bael's favor may gain the power of invisibility and the art of betrayal. To invoke him, his symbol must be traced on the ground, surrounded by black candles. The summoner must recite:

''Bael, Lord of Secrets, emerge from the East and lend me your power''.

In return, Bael will demand the sacrifice of a valuable secret, a confession that the summoner has never shared with anyone.
</p>
<br>
<h3>Protection:</h3>
<p>
However, one who seeks Bael's favor will eventually find themselves betrayed, as the demon always takes more than he gives. To protect oneself from him, it is advisable to wear a blessed silver pendant and recite a daily protection enchantment at dawn. In cases of extreme need, the only known way to expel him is through the ''Ritual of Sacred Fire,'' in which laurel and rosemary must be burned while reciting ancient exorcism words.
</p>
