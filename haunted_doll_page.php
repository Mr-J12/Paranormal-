    <h2>The Haunted Doll</h2>
    <p>Though she doesn’t share the same porcelain skin and lifelike features as her cinematic counterpart, the Annabelle doll that lives in the Occult Museum of famed paranormal investigators Ed and Lorraine Warren, the pair that worked on the case, is made all the more creepy by how ordinary she appears.

        <br><br>Annabelle’s stitched features, including her half-smile and bright orange triangular nose, evoke memories of childhood toys and simpler times.
        
        <br><br>If you could ask Ed and Lorraine Warren (though Ed died in 2006 and Lorraine died in early 2019), they would tell you that the stark warnings scrawled across Annabelle’s glass case are more than necessary.

        <br><br>According to the well-known paranormal investigators, the doll is responsible for two near-death experiences, one fatal accident, and a string of demonic activities that lasted some 30 years.
        
        <br><br>The first of these infamous hauntings can allegedly be traced back to 1970, when Annabelle was brand new. The story was told to the Warrens by two young women and was retold for years after by the Warrens themselves.
    
        <br><br>As the story goes, the Annabelle doll had been a gift to a young nurse named Donna (or Deirdre, depending on the source) from her mother for her 28th birthday. Donna, apparently thrilled with the gift, brought it back to her apartment that she shared with another young nurse named Angie.

        <br><br>At first, the doll was an adorable accessory, sitting on a sofa in the living room and greeting visitors with her colorful visage. But before long, the two women began to notice that Annabelle seemed to move about the room of her own accord.
        
        <br><br>Donna would sit her on the living room sofa before leaving for work only to come home in the afternoon and find her in the bedroom, with the door shut.
    
        <br><br>Donna and Angie then started finding notes left throughout the apartment reading “Help Me.” According to the women, the notes were written on parchment paper, which they did not even keep in their home.
    
        <br><br>Furthermore, Angie’s boyfriend, known only as Lou, was in the apartment one afternoon while Donna was out and heard rustling in her room as if someone had broken in. Upon inspection, he found no sign of forced entry but found the Annabelle doll lying face down on the ground (other versions of the story say he was attacked upon waking up from a nap).
    
        <br><br>Suddenly, he felt a searing pain on his chest and looked down to find bloody claw marks running across it. Two days later, they had vanished without a trace.

        <br><br>Following Lou’s traumatic experience, the women invited a medium over to help solve their seemingly paranormal problem. The medium held a seance and told the women that the doll was inhabited by the spirit of a deceased seven-year-old named Annabelle Higgins, whose body had been found years earlier on the site where their apartment building had been built.
        
        <br><br>The medium claimed that the spirit was benevolent and simply wanted to be loved and cared for. The two young nurses reportedly felt bad for the spirit and consented to allow her to take up permanent residence in the doll.</p>
