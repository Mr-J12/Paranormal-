    <h1>Decarabia</h1>
<h3>Origin:</h3>
<p>
Decarabia, also known as Carabia, was once the guardian of the heavens, a being who watched over the constellations and winged creatures. His rebellion was not driven by pride or envy, but by an insatiable curiosity. He thirsted for the darkest secrets of the cosmos, and in his quest for forbidden knowledge, he directly disobeyed celestial orders. His transgression did not go unnoticed, and for his thirst for knowledge, he was cast out of the firmament, condemned to wander the abyss as a banished star.
</p>
<br>
<h3>Characteristics:</h3>
<p>
Decarabia has the peculiarity of initially appearing as a bright star trapped in a pentacle. However, under the command of an exorcist, he can take on a dazzling yet intangible human form. His abilities are unique; he has the power to reveal the hidden virtues of birds and precious gems. Additionally, he can summon illusions of birds of all kinds, which sing and drink as if they were real, a hypnotic and mysterious sight.
</p>
<br>
<h3>Dominion:</h3>
<p>
Decarabia rules over the secret knowledge of nature, especially that of birds and precious stones. Those who seek to decipher the hidden mysteries within these entities often seek his guidance. Furthermore, he has the ability to influence dreams and visions, offering revelations to those willing to listen.
</p>
<br>
<h3>Legions:</h3>
<p>
As the Grand Marquis of hell, Decarabia commands 30 legions of spirits. These spirits are ethereal entities, often associated with constellations and birds, who wander the world collecting secrets and observing from the shadows.
</p>
<br>
<h3>Prophecies:</h3>
<p>
It is said that when the stars in the sky align in a pattern never before seen, Decarabia will emerge from the shadows, releasing his knowledge to the world. This revelation will bring both wonders and catastrophes, changing the way humanity views the universe.
</p>
<br>
<h3>Invocation:</h3>
<p>
Invoking Decarabia promises hidden knowledge and mystical revelations. Those who seek his favor must draw a symbol on the ground under the light of a full moon and recite:

''Banished star, guardian of secrets, reveal the hidden truths and guide my way''.

In exchange, Decarabia demands an offering: a precious gem or a rare feather, objects that contain the essence of his domain.
</p>
<br>
<h3>Protection:</h3>
<p>
Although Decarabia is not inherently malevolent, the knowledge he imparts can be overwhelming and dangerous for the unprepared. Visions can become unbearable or lead to madness. To protect oneself from his influences, it is recommended to carry a clear quartz amulet, which disperses and purifies astral energies. In case of feeling overwhelmed, immersing the amulet in saltwater and reciting a purification mantra can help break his influence.
</p>
