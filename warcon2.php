 <h1>Eligos</h1>
<h3>Origin:</h3>
<p>
Before his fall, Eligos, also known as Abigor or Eligor, was the celestial guardian of secrets and mysteries. His privileged position granted him access to the most hidden designs. However, his obsession with power and insatiable curiosity led him to challenge the boundaries imposed by the divine. After attempting to reveal these secrets to other entities, he was punished and expelled from the celestial realm, becoming a demon stripped of his original purity.
</p>
<br>

<h3>Characteristics:</h3>
<p>
An imposing figure, Eligos appears before mortals as a well-armed knight, wielding a lance that pierces truths and a snake that symbolizes his cunning and deceit. He prefers to manifest at dusk when the line between light and darkness fades. He has the gift of premonition and can anticipate conflicts and wars, influencing the fate of armies and nations.
</p>
<br>
<h3>Dominion:</h3>
<p>
Eligos delights in manipulating people of power, causing leaders to fall under his charm and alter the course of history. His ability to uncover secrets makes him a formidable ally or enemy, depending on his intentions. With a whisper, he can ignite the sparks of love or hatred in the hearts of the most influential.
</p>
<br>
<h3>Legions:</h3>
<p>
Commanding 60 legions of spirits, each specialized in the art of war and deceit, Eligos has a vast network of followers operating in the shadows. These spirits, following his orders, can influence decisions, alter events, and ensure that Eligos's designs are carried out.</p>
</p>
<br>
<h3>Prophecies:</h3>
<p>
There are rumors of a future in which Eligos will lead a rebellion against other demons, seeking to consolidate his power and control all infernal planes. This civil war in the depths would unleash unprecedented chaos on Earth, leaving humanity trapped in the midst of the conflict.
</p>
<br>
<h3>Invocation:</h3>
<p>
Summoning Eligos promises hidden knowledge and advantages in conflicts and ambitions. To call upon him, one must draw his symbol surrounded by a circle of black candles. At midnight, the summoner must recite:

''Eligos, Guardian of Secrets, I call upon you. I offer my in exchange for your guidance and wisdom''.

However, this pact requires the summoner to reveal a dark secret from their past to Eligos.
</p>
<br>
<h3>Protection:</h3>
<p>
Alliance with Eligos always comes at a price. Secrets revealed to him are never truly safe and can be used against the summoner. To protect oneself from his influence, it is vital to wear an iron amulet forged under the new moon and recite daily purification invocations. If one wishes to sever the bond with Eligos, a ritual in a sacred place under the guidance of a spiritual protector is essential 
</p>
