<h1>Amon</h1>
    <h3>Origin:</h3>
<p>
Before time was written, Amon was a high priest who defied the gods, seeking the power of forbidden knowledge. His audacity came at a great cost, and he was transformed into a demon, marked to be an eternal marquis of the underworld, punished for his ambition and disdain for boundaries.
</p>
<br>

<h3>Characteristics:</h3>
<p>
Amon can appear as a wolf with a serpent's tail, spewing voracious flames. However, if commanded, he takes on the form of a man with a grotesque blend of animal features: prominent canine teeth and the head of a crow. This appearance, besides being terrifying, serves as a reminder of the price of unwanted knowledge.
</p>
<br>
<h3>Dominion:</h3>
<p>
Amon, with his ability to know the past and the future, toys with human life, creating conflicts and resolving them at his whim. He has the power to influence minds, causing old friends to become sworn enemies and the deepest enmities to reconcile unexpectedly.
</p>
<br>
<h3>Legions:</h3>
<p>
With 40 Legions of Spirits under his command, Amon reigns over those who seek forbidden knowledge and hidden powers. While he does not have a defined realm, his influence is palpable in dark libraries and forgotten corners where ancient secrets are kept.
</p>
<br>
<h3>Prophecies:</h3>
<p>
There are rumors that, in a future era, Amon will unleash all his knowledge upon the world, flooding it with forgotten truths and dark secrets, leading humanity to its zenith or ruin, depending on how it chooses to use that information.
</p>
<br>
<h3>Invocation:</h3>
<p> 
Those seeking Amon's counsel seek answers to mysteries of the past or visions of the future. By drawing his special symbol and reciting:

''Amon, guardian of secrets, reveal the hidden and guide my path,''

he may grant an audience. However, the price is high: a valuable memory or an untold secret.
</p>
<br>
<h3>Protection:</h3>
<p>
Those who deal with Amon may find themselves ensnared in a web of intrigues and betrayals. Protection against him comes in the form of unwavering truth and the avoidance of secrets. It is said that wearing an obsidian amulet and reciting daily protection mantras can keep his influence at bay. Sincerity and frankness are the true weapons against this cunning demon
</p>
