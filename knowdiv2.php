    <h1>Agreas</h1>
<h3>Origin:</h3>
<p>
Agares, before his fall, was an elderly sage, revered in the land of dunes for his profound knowledge of languages and dialects. His thirst for power and control over words led him to a dark destiny. His soul, trapped in the intertwined game of languages he once mastered, was condemned to wander the underworld, transforming into the Duke of the eastern region of Hell.
</p>
<br>

<h3>Characteristics:</h3>
<p>
Deceptively frail in appearance, this demon presents himself as an elderly man riding upon a colossal crocodile, with a majestic falcon perched on his fist. His eyes, however, reflect millennia of wickedness and cunning. He is known for controlling movement, causing fugitives to return and those at rest to feel compelled to flee.
</p>
<br>
<h3>Dominion:</h3>
<p>
Agares controls communication, mastering all existing languages. However, his greatest pleasure lies in twisting words and teaching immoral expressions. He possesses the ability to destroy dignities, both in the realm of the supernatural and the earthly, leaving those who once stood tall in disgrace.
</p>
<br>
<h3>Legions:</h3>
<p>
Under his command, 31 legions of demons march, specializing in distorting communications and sowing chaos through misunderstandings. From his stronghold in the infernal east, he has influence not only in the underworld but also in all places where words hold power.
</p>
<br>
<h3>Prophecies:</h3>
<p>
It is said that in the not-so-distant future, a great misunderstanding will arise in the mortal world, a linguistic conflict that threatens to trigger wars and discord, and it will be Agares who is behind it, weaving the words and reveling in the chaos.
</p>
<br>
<h3>Invocation:</h3>
<p>
One who summons Agares can acquire the gift of understanding languages, being able to communicate and comprehend any dialect. However, the price is high. To invoke him, one must draw his symbol on a parchment. Surrounding this symbol, candles of blood. The summoner must recite:

''Agares, Duke of Speech, grant me mastery of tongues''.

In return, the demon will demand the summoner's voice, condemning them to be a stutterer or to distort their words.
</p>
<br>
<h3>Protection:</h3>
<p>
Summoning Agares can result in a life of silence or the distortion of your own words. It is vital to protect oneself by carrying a pendant with the figure of a falcon. If someone falls into his linguistic traps, the only known remedy is the ''Ritual of Clarity,'' in which one must immerse their tongue in holy water and recite sacred words at dawn.
</p>
