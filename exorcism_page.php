    <h2>A Deadly Exorcism</h2>
    <p>Seven members of a family who were accused of imprisoning and killing a man during a three-day "exorcism" have been cleared of all charges.

        <br><br>Kennedy Ife, 26, died after he was allegedly restrained at a house in Enfield, north London, in August 2016.
        
        <br><br>The Old Bailey heard his parents and brothers mistakenly believed he was "possessed by evil spirits" so they set about "curing" him.
        
        <br><br>All seven accused were found not guilty of manslaughter.
        
        <br><br>Mr Ife's parents, Kenneth, 64, and Josephine, 56, along with his brothers Roy, 33, Harry, 32, Colin, 26, Samuel, 20, and Daniel, 20, were also cleared of false imprisonment and causing or allowing the death of a vulnerable adult.
        
        <br><br>'Domestic issue'
        <br><br>Jurors heard the family held deep religious beliefs and were connected with the Jesus Sanctuary Ministries in south-east London.

        <br><br>On 19 August, Kennedy became aggressive, bit his father and threatened to cut off his own penis so his mother sought advice from a minister rather than seeking medical help.
        
        <br><br>The family then set about attempting to "cure" Kennedy through restraint and prayer over the next three days, the court was told.

<br><br>Roy Ife said his brother's actions were "shocking to hear" but he did not consider calling professional help because it was a "domestic issue".

<br><br>Kenneth Ife told jurors he ordered his sons to take shifts and use "overwhelming force" but denied that an "association with cults, occults and secret societies" played any part in the death.

<br><br>The 26-year-old was pronounced dead after Harry Ife called emergency services saying his brother had been complaining of dehydration.</p>
