    <h2>The Amityville horror</h2><br>
    <p>The book describes the house at 112 Ocean Avenue as remaining empty for 13 months after the DeFeo murders. In December 1975, George and Kathleen Lutz bought the house for what was considered to be a bargain price of $80,000. The five-bedroom house was built in Dutch Colonial style, and had a distinctive gambrel roof. It also had a swimming pool and a boathouse, as it was located on a canal. George and Kathy married in July 1975, and each had their own homes, but they wanted to start fresh with a new property. Kathy had three children from a previous marriage: Daniel, 9, Christopher, 7, and Melissa (Missy), 5. They also owned a crossbreed Malamute/Labrador dog named Harry. During their first inspection of the house, the real estate broker told them about the DeFeo murders and asked if this would affect their decision. After discussing the matter, they decided that it was not a problem.

        <br><br>Lutz family moved in on December 18, 1975. Much of the DeFeo family's furniture was still in the house, because it was included for $400 as part of the deal. A friend of George Lutz learned about the history of the house and insisted on having it blessed. At the time, George was a non-practicing Methodist and had no experience of what this would entail. Kathy was a non-practicing Catholic and explained the process. George knew a Catholic priest named Father Ray who agreed to carry out the house blessing (in Anson's book, real-life priest Father Ralph J. Pecoraro is referred to as Father Mancuso for privacy reasons).
        
        <br><br>Father Mancuso was a lawyer, judge of the Catholic Court and psychotherapist who lived at the local Sacred Heart Rectory. He arrived to perform the blessing while George and Kathy were unpacking their belongings on the afternoon of December 18, 1975, and went into the building to carry out the rites. When he flicked the first holy water and began to pray, he heard a masculine voice demand that he "get out". When leaving the house, Father Mancuso did not mention this incident to either George or Kathy. On December 24, 1975, Father Mancuso called George Lutz and advised him to stay out of the second floor room where he had heard the mysterious voice, the former bedroom of Marc and John Matthew DeFeo, that Kathy planned to use as a sewing room, but the call was cut short by static. Following his visit to the house, Father Mancuso allegedly developed a high fever and blisters on his hands similar to stigmata. At first George and Kathy experienced nothing unusual in the house. Talking about their experiences subsequently, they reported that it was as if they "were each living in a different house".
        
        <br><br>By mid-January 1976, after another attempt at a house blessing by George and Kathy, they experienced what would turn out to be their final night in the house. The Lutzes declined to give a full account of the events that took place on this occasion, describing them as "too frightening".
        
        <br><br>After getting in touch with Father Mancuso, the Lutzes decided to take some belongings and stay at Kathy's mother's house in nearby Deer Park, New York, until they had sorted out the problems with the house. They claimed that the phenomena followed them there, with the final scene of Anson's book describing "greenish-black slime" coming up the staircase towards them. On January 14, 1976, George and Kathy Lutz, with their three children and their dog Harry, left 112 Ocean Avenue, leaving all of their possessions behind. The next day, a mover arrived to remove the possessions to send to the Lutzes. He reported no paranormal phenomena while inside the house.
        
        <br><br>The book was written after Tam Mossman, an editor at the publishing house Prentice Hall, introduced George and Kathy Lutz to Jay Anson. The Lutzes did not work directly with Anson, but submitted around 45 hours of tape-recorded recollections to him, which were used as the basis of the book. Estimates of the sales of the book are around 10 million copies from its numerous editions. Anson is said to have based the title of The Amityville Horror on "The Dunwich Horror" by H. P. Lovecraft, which was published in 1929.</p>
