    <h1>Buer</h1>
<h3>Origin:</h3>
<p>
Before darkness shrouded the earth and the skies were resplendent, Buer was a celestial scholar destined to be a moral and logical guide for young angels. However, his obsession with the herbs and plants of Eden led him to forbidden knowledge. By attempting to merge the essence of plants with divine logic, he caused an imbalance in celestial harmony. As punishment, he was banished, condemned to wander in the shadow of Sagittarius, where his essence merged with the zodiac sign.
</p>
<br>
<h3>Characteristics:</h3>
<p>
Buer possesses unparalleled knowledge of moral and natural philosophy, as well as logical art. He can teach those who dare to invoke him the healing virtues of all herbs and plants. However, his manifestation is terrifying: he emerges as a twisted figure, half-man and half-beast, with centaur horns and eyes that burn with the fire of a thousand suns. His voice is soft but with a tone that can chill the blood.
</p>
<br>
<h3>Dominion:</h3>
<p>
Buer rules over the inherent rebellion in humans, having the power to heal these internal insurgencies. He has the ability to influence the human mind, directing their passions and quelling their rebellions. He can manipulate elements of nature, especially herbs and plants, using his essence to heal or harm.
</p>
<br>
<h3>Legions:</h3>
<p>
This demon rules over 50 legions of spirits, composed of both earthly entities and beings that once inhabited the celestial realm. These legions manifest as shadows, always around Buer, ready to carry out his orders.
</p>
<br>
<h3>Prophecies:</h3>
<p>
It is said that during a total eclipse coinciding with Sagittarius, Buer will gain the power to influence all of humanity simultaneously, leading it to a philosophical renaissance or its total destruction.
</p>
<br>
<h3>Invocation:</h3>
<p>
Those brave or desperate enough to wish to invoke Buer must draw his special symbol on the earth, surrounded by rare herbs. Reciting the prayer,

''Buer, guardian of forbidden wisdom, come to me and grant me your knowledge,''

can summon him. In return, the summoner must offer a personal secret, something never shared with another human being. If Buer is satisfied, he will share his knowledge.
</p>
<br>
<h3>Protection:</h3>
<p>
Invoking Buer is not without dangers. While he can bestow wisdom, he can also extract something from the summoner, leaving them empty or devoid of emotions. To protect oneself from his influences, it is advisable to wear an amulet made of sage and laurel and recite a daily prayer of gratitude to the universe. If someone needs to expel him, a circle of salt and rosemary, along with a constant recitation of positive mantras, can weaken his presence until it disappears.
</p>
