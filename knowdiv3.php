    <h1>Alloces</h1>
<h3>Origin:</h3>
<p>
Before being known as Alloces, this demon was a brilliant celestial being who served with passion in the high choirs of angels. For a time, he was one of those tasked with overseeing astral cycles and the stars, maintaining celestial order. However, it was knowledge, the unending quest for the hidden secrets of the universe, that led to his fall. Believing he could influence and alter the destiny of worlds and creatures, he directly challenged the divine plan and was cast into the abyss, transforming into a duke of hell.
</p>
<br>

<h3>Characteristics:</h3>
<p>
Alloces is a master of the sciences and astronomy. He appears as an imposing soldier mounted on a powerful horse, his leonine face burning in scarlet hues, and his eyes dripping with flames. His voice is deep and resonant, capable of sending chills down the spines of those who hear it. Though his appearance is terrifying, the wisdom he radiates is immense and tempting.
</p>
<br>
<h3>Dominion:</h3>
<p>
Alloces rules over knowledge and the sciences. He has the power to influence the human mind, bestowing outstanding intellectual abilities and hidden knowledge. He can also reveal the secrets of the cosmos, from stellar orbits to the deepest mysteries of black holes.
</p>
<br>
<h3>Legions:</h3>
<p>
Under his command, Alloces controls 36 legions of spirits, each specialized in different branches of knowledge and wisdom. These entities act as his librarians and teachers, collecting and distributing information in his vast infernal realm.
</p>
<br>
<h3>Prophecies:</h3>
<p>
It is said that at the peak of a rare astral alignment, Alloces will gain enough power to alter the course of human destiny, leading civilization into a new golden age of knowledge... or its complete ruin.
</p>
<br>
<h3>Invocation:</h3>
<p>
Invoking Alloces offers the precious gift of knowledge. Those who wish to do so must draw his special symbol in a place surrounded by astronomical instruments. While reciting the following prayer:

''Oh, great Alloces, bearer of astral wisdom, reveal to me the secrets I desire. I promise to respect and honor the knowledge you offer''.

In exchange for the demon's favor, the summoner must offer a manuscript or book of incalculable value, preferably on sciences or occult arts.
</p>
<br>
<h3>Protection:</h3>
<p>
Challenging Alloces or misusing the knowledge granted can result in a curse of perpetual ignorance. To protect oneself from his influence, it is advisable to carry a clear quartz amulet blessed under the new moon and immerse it in rosewater for a night. If it becomes necessary to expel his presence, laurel should be burned, and protective astral mantras recited.
</p>
